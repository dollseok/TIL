data = input()

if data == 'A+':
    print('4.3')
elif data == 'A0':
    print('4.0')
elif data == 'A-':
    print('3.7')
elif data == 'B+':
    print('3.3')
elif data == 'B0':
    print('3.0')
elif data == 'B-':
    print('2.7')
elif data == 'C+':
    print('2.3')
elif data == 'C0':
    print('2.0')
elif data == 'C-':
    print('1.7')
elif data == 'D+':
    print('1.3')
elif data == 'D0':
    print('1.0')
elif data == 'D-':
    print('0.7')
else:
    print('0.0')